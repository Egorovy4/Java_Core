package lesson6;

public class PlaneDriving {
	public void moveUp(int distance) {
		System.out.println("Plane is moving up " + distance + " km !");
	}
	
	public void moveDown(int distance) {
		System.out.println("Plane is moving down " + distance + " km !");
	}
	
	public void moveLeft(int distance) {
		System.out.println("Plane is moving left " + distance + " km !");
	}
	
	public void moveRight(int distance) {
		System.out.println("Plane is moving right " + distance + " km !");
	}
}
